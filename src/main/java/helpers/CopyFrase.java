package helpers;

public class CopyFrase {
	private String name;
	private String frase;

	public CopyFrase(String name, String frase) {
		this.name = name;
		this.frase = frase;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getFrase() {
		return frase;
	}

	public void setFrase(String frase) {
		this.frase = frase;
	}
}
